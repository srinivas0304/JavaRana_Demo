package switchcase_Concept;

public class Switch_Case 
{
	public static void main(String[] args) 
	{
		String tiffen="Dosa";
		
		switch(tiffen)
		{
		case "Dosa":
		{
			System.out.println("Monday tiffen");
			break;
		}
		case "Idly":
		{
			System.out.println("Tuesday tiffen");
			break;
		}
		case "Wada":
		{
			System.out.println("Wednesday tiffen");
			break;
		}
		case "Upma":
		{
			System.out.println("Thrusday tiffen");
			break;
		}
		case "Sambar":
		{
			System.out.println("Every day");
			break;
		}
		
		}
	}
}
